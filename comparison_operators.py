# BOOLEAN COMPARISON OPERATORS

# Greaer than: >
x = 3 > 2
print(x) # True

# Less than: <
x = 3 < 2
print(x) # False

# Greater than or equal to: >=
x = 3 >= 2
print(x) # True

# Less than or equal to: <=
x = 3 <= 2
print(x) # False

# Equal to: ==
x = 3 == 2
print(x) # False

# Not equal to: !=
x = 3 != 2
print(x) # True