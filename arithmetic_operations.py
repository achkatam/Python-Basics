print(10 + 2)

print(10 - 2)

print(10 * 2)

# Returns floating point number
print(10 / 2) # 5.0

# Returns integer
print(10 // 2)  # 5

# Returns the remainder
print(10 % 2)
print(10 % 3)

# Power a number
print(10 ** 3)
   
# Augmented assignment operator
x = 10
x += 3
print(x)

# Decreasing operator
x = 10
x -= 3
print(x)